
class MorseTiming {
  final int dotMs;
  MorseTiming(int wpm) : dotMs = (1200 / (wpm <= 0 ? 20 : wpm)).round();

  int get dashMs => dotMs * 3;

  int get elementPause => dotMs;

  int get letterPause => dotMs * 3;

  int get wordPause => dotMs * 7;
}

